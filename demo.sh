#!/bin/bash

# Check the Ubuntu version and handle unsupported versions
if ! [[ "18.04 20.04 22.04 23.04" == *"$(lsb_release -rs)"* ]];
then
    echo "Ubuntu $(lsb_release -rs) is not currently supported.";
    exit;
fi

# Add Microsoft's repository for MSSQL drivers/tools
curl https://packages.microsoft.com/keys/microsoft.asc | sudo tee /etc/apt/trusted.gpg.d/microsoft.asc
curl https://packages.microsoft.com/config/ubuntu/$(lsb_release -rs)/prod.list | sudo tee /etc/apt/sources.list.d/mssql-release.list

# Update packages and install required Microsoft tools and libraries
sudo apt-get update
sudo ACCEPT_EULA=Y apt-get install -y msodbcsql18
# optional: for bcp and sqlcmd
sudo ACCEPT_EULA=Y apt-get install -y mssql-tools18
echo 'export PATH="$PATH:/opt/mssql-tools18/bin"' >> ~/.bashrc
source ~/.bashrc
# optional: for unixODBC development headers
sudo apt-get install -y unixodbc-dev

# Fetch the Llama model file using wget
if [ ! -f llama-2-7b.Q4_K_M.gguf ]; then
    wget https://huggingface.co/TheBloke/Llama-2-7B-GGUF/resolve/main/llama-2-7b.Q4_K_M.gguf
fi

# Install Python dependencies from requirements.txt
pip install -r requirements.txt

# Execute the Python script
python llama_text_generation.py

# Execute the backend management web server
cd chatbo_web
python manage.py runserver 8080